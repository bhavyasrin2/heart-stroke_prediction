import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

from google.colab import drive
drive.mount('/content/drive')

data=pd.read_csv("/content/drive/MyDrive/machine learning lab/heartstroke/stroke_data.csv")
data.head()

data.describe()

data.info()

data["sex"].fillna(np.mean(data["sex"]), inplace = True)

data.info()

sns.heatmap(data.corr())

data.drop('avg_glucose_level',axis=1,inplace=True)

plt.scatter(data['bmi'],data['stroke'])

data.hist()

data.head()

X=data.iloc[:,:-1]
Y=data.iloc[:,-1]

from sklearn.model_selection import train_test_split
X_train,X_test,Y_train,y_test=train_test_split(X.values,Y,random_state=99)

from sklearn.tree import DecisionTreeClassifier
dt_e = DecisionTreeClassifier(
            criterion = "gini")
dt_e.fit(X_train,Y_train)

print(dt_e.feature_importances_)

Y_pred=dt_e.predict(X_test)

from sklearn.metrics import confusion_matrix
cf1=confusion_matrix(y_test,Y_pred)
print(cf1)

from sklearn.metrics import classification_report
print(classification_report(y_test,Y_pred))

import pickle

pickle.dump(dt_e,open('iri3.pkl','wb'))

